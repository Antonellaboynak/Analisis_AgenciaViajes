-- RESUMEN ANUAL DE KPIs FINANCIEROS
SELECT
    COUNT(ID_RES)                                                        AS TOTAL_RESERVAS,
    ROUND(SUM(VENTAUS), 2)                                               AS VENTAUS_TOTAL,
    ROUND(SUM(COSTOUS), 2)                                               AS COSTOUS_TOTAL,
    ROUND(AVG(VENTAUS), 2)                                               AS VENTAUS_PROMEDIO,
    ROUND(SUM(VENTAUS - COSTOUS - GASTOSUS), 2)                          AS MARGEN_TOTAL,
    ROUND(
        SUM(VENTAUS - COSTOUS - GASTOSUS) / NULLIF(SUM(VENTAUS), 0) * 100
    , 2)                                                                 AS MARGEN_PCT,
    COUNT(CASE WHEN (VENTAUS - COBROSUS) > 0 THEN 1 END)                AS RESERVAS_NOCOBRADAS,
    ROUND(
        COUNT(CASE WHEN (VENTAUS - COBROSUS) > 0 THEN 1 END) / COUNT(ID_RES) * 100
    , 2)                                                                 AS RESERVAS_NOCOBRADAS_PCT,
    ROUND(SUM(CASE WHEN (VENTAUS - COBROSUS) > 0 THEN (VENTAUS - COBROSUS) ELSE 0 END), 2)
                                                                         AS MONTO_NOCOBRADO_TOTAL
FROM listado_agencia_resumido;

-- -- ANĂLISIS MENSUAL DE RESERVAS Y KPIs FINANCIEROS
SELECT
    YEAR(FEC_SAL)                                   AS ANIO,
    MONTH(FEC_SAL)                                  AS MES_NUM,
    DATE_FORMAT(FEC_SAL, '%Y-%m')                   AS MES,
    COUNT(ID_RES)                                   AS TOTAL_RESERVAS,
    ROUND(AVG(COUNT(ID_RES)) OVER (), 2)            AS PROMEDIO_RESERVAS_MES,
    ROUND(SUM(VENTAUS), 2)                          AS VENTAUS_TOTAL,
    ROUND(SUM(COSTOUS), 2)                          AS COSTOUS_TOTAL,
    ROUND(SUM(VENTAUS - COSTOUS - GASTOSUS), 2)     AS MARGEN_TOTAL,
    ROUND(
        SUM(VENTAUS - COSTOUS - GASTOSUS) / NULLIF(SUM(VENTAUS), 0) * 100
    , 2)                                            AS MARGEN_PCT
 FROM listado_agencia_resumido
WHERE FEC_SAL IS NOT NULL
GROUP BY ANIO, MES_NUM, MES
ORDER BY ANIO, MES_NUM;

