-- CTE: RANKING DE VENDEDORES CON CATEGORĂA DE PERFORMANCE
WITH ventas_por_vendedor AS (
    SELECT
        NOM_USU                                             AS VENDEDOR,
        COUNT(ID_RES)                                       AS TOTAL_RESERVAS,
        ROUND(SUM(VENTAUS), 2)                              AS VENTAUS_TOTAL,
        ROUND(AVG(VENTAUS), 2)                              AS TICKET_PROMEDIO,
        ROUND(SUM(VENTAUS - COSTOUS - GASTOSUS), 2)         AS MARGEN_TOTAL,
        ROUND(SUM(VENTAUS - COSTOUS - GASTOSUS) 
            / NULLIF(SUM(VENTAUS), 0) * 100, 2)             AS MARGEN_PCT
    FROM listado_agencia_resumido
    WHERE NOM_USU IS NOT NULL
    GROUP BY NOM_USU
)
SELECT
    VENDEDOR,
    TOTAL_RESERVAS,
    VENTAUS_TOTAL,
    TICKET_PROMEDIO,
    MARGEN_TOTAL,
    MARGEN_PCT,
    RANK() OVER (ORDER BY VENTAUS_TOTAL DESC)               AS RANKING,
    CASE
        WHEN VENTAUS_TOTAL >= (SELECT AVG(VENTAUS_TOTAL) * 1.2 FROM ventas_por_vendedor) THEN 'ALTO RENDIMIENTO'
        WHEN VENTAUS_TOTAL >= (SELECT AVG(VENTAUS_TOTAL) FROM ventas_por_vendedor)       THEN 'RENDIMIENTO MEDIO'
        ELSE 'BAJO RENDIMIENTO'
    END                                                     AS CATEGORIA
FROM ventas_por_vendedor
ORDER BY RANKING;

-- WINDOW FUNCTIONS: EVOLUCIĂN MENSUAL CON ACUMULADO Y VARIACIĂN
SELECT
    DATE_FORMAT(FEC_SAL, '%Y-%m')                           AS MES,
    COUNT(ID_RES)                                           AS RESERVAS_MES,
    ROUND(SUM(VENTAUS), 2)                                  AS VENTAUS_MES,
    ROUND(SUM(VENTAUS - COSTOUS - GASTOSUS), 2)             AS MARGEN_MES,
    ROUND(SUM(SUM(VENTAUS)) 
        OVER (ORDER BY DATE_FORMAT(FEC_SAL, '%Y-%m')), 2)   AS VENTAUS_ACUMULADO,
    ROUND(SUM(VENTAUS) - LAG(SUM(VENTAUS)) 
        OVER (ORDER BY DATE_FORMAT(FEC_SAL, '%Y-%m')), 2)   AS VARIACION_VS_MES_ANT,
    ROUND(
        (SUM(VENTAUS) - LAG(SUM(VENTAUS)) 
            OVER (ORDER BY DATE_FORMAT(FEC_SAL, '%Y-%m')))
        / NULLIF(LAG(SUM(VENTAUS)) 
            OVER (ORDER BY DATE_FORMAT(FEC_SAL, '%Y-%m')), 0) * 100
    , 2)                                                    AS VARIACION_PCT
FROM listado_agencia_resumido
WHERE FEC_SAL IS NOT NULL
GROUP BY MES
ORDER BY MES;

